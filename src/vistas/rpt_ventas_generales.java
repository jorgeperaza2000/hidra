    private void jTextFieldFechaDesdeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldFechaDesdeKeyPressed
        if ( evt.getKeyCode() == 10 ) {
            if ( !"".equals(this.jTextFieldFechaDesde.getText()) ) {
                if ( !"".equals(this.jTextFieldFechaHasta.getText()) ) {
                    this.jButtonBuscar.doClick();
                } else {
                    this.jTextFieldFechaHasta.requestFocus(true);
                }
            }
        }
    }//GEN-LAST:event_jTextFieldFechaDesdeKeyPressed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        try {
            ventas_generales.obtenerVentasGenerales(this.jTableVentasGenerales, this.jTextFieldFechaDesde, this.jTextFieldFechaHasta);
        } catch (IOException | ParseException ex) {
            Logger.getLogger(rpt_ventas_generales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

